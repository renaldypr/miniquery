/*
 * ----------------------------------------------------------------------------
 * Element Selector
 * ----------------------------------------------------------------------------
 */
class SweetSelector {
  static select(tag) {
    if (tag[0] === '#') {
      var element = document.getElementById(tag.slice(1));
    } else if (tag[0] === '.') {
      var element = document.getElementsByClassName(tag.slice(1));
    } else {
      var element = document.getElementsByTagName(tag);
    }
    return element;
  }
}

